def chop_series(data, dep_col, condition=lambda f: f<0 ):
    """
    Returns only those rows in data with an index that is lower than the row
    with lowest index in which condition() is evaluated as true. Condition
    can be any method that returns a boolean, data is an iterable that supports
    [a,b]-indexing (typically a numpy array), and dep_col is the column on 
    which condition() is evaluated.
    """
    for i in range(0,len(data[:,dep_col])):
        if condition(data[i,dep_col]):
            return data[0:i,:]
        #
    #
    return data
