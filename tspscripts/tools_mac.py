def gaussian_F_q(R_s_sq, chain_length, qrange=np.arange(0,50,0.1)):
    """
    Takes an numpy array R_s_sq with in the first column a monomer index and in
    the second column a mean squared average distance divided by the monomer
    index, an integer chain_length, and, optionally, qrange, a numpy array. 
    Calucalates F_q under the Gaussian assumption, namely that the scattering
    can be described by the averages of the separations, absolving the necessity
    of weighing all measured separations. Returns two lists (qrange, Gaussian_F_qs)
    as one tuple, with the calculated F_q as function of q.
    """
    terms=[ [] for q in qrange ]
    Gaussian_F_qs=[ [] for q in qrange ]
    for q_idx in range(0,len(qrange)):
        for i in range(1, chain_length + 1):
            for j in range(1, chain_length + 1):
                s=abs(i-j)
                if s>0:
                    current_R_s_sq=R_s_sq[index_nearest(R_s_sq[:,0], s), 1]*s
                    term=np.exp(-1./6*qrange[q_idx]**2*current_R_s_sq)
                elif s==0:
                    term=1
                terms[q_idx].append(term)
            #
        #
        Gaussian_F_qs[q_idx]=sum(terms[q_idx])/chain_length
    #
    return (qrange, Gaussian_F_qs)
