def rescale_S_qt_dRPA(time, S_qt, F_q, S_0, q, W, b):
    """ rescale_S_qt_dRPA(time, S_qt, F_q, S_0) returns a Nrows*2 numpy array with in first column a rescaled time
        using tq^4*A(W,b) and in second column a rescaled dsf using dRPA scaling S_qt*F_q/(S_0)**2 """
